package Batch159.practice.practice04;

public class Q01_Ascii_OneToHundred {

    public static void main(String[] args) {


      // Print on the console numbers from 1 to 100. But do not use any number in your code!

        System.out.println('a'+0);      //97
        System.out.println('d'+0);      //100
        System.out.println('d'/'d' + "-" + (int) 'd');

        for(int i='d'/'d'; i<='d'; i++) {
            System.out.print(i + " ");
        }


        // Print on the console numbers from 0 to 100. But do not use any number in your code!

        for(int i='d'-'d'; i<='d'; i++) {
            System.out.print(i + " ");
        }

    }
}
