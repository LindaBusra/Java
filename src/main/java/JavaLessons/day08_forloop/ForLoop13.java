package JavaLessons.day08_forloop;

public class ForLoop13 {
    public static void main(String[] args) {




//Example-1-Type code to find the sum of the integers from 12 to 231

        int sum = 0;

        for(int i=12; i<232; i++) {
            sum = sum + i;
        }

        System.out.println("The sum is"+ sum);






//Example-2-Type code to find the multiplication of the integers from 3 to 5

        int mul = 1;

        for(int i=3; i<6; i++) {
            mul = mul*i;
        }
        System.out.println(mul);








    }
}
